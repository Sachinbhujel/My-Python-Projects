print("This is a basic calculator")
firstNumber = int(input("enter your first number :"))
secondNumber = int(input("Enter your second number : "))
operator = input("Enter your operation : ")
	
if operator == "+":
	print("Answer is" , firstNumber+secondNumber)
elif operator == "-":
	print("Answer is" ,firstNumber-secondNumber)
elif operator == "/":
	print( "Answer is" ,firstNumber/secondNumber)
elif operator == "*":
	print(" Answer is" , firstNumber*secondNumber)

	
	
	
